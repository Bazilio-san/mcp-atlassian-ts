import fs from 'fs';
import path from 'path';

/**
 * Удаление файла _postinstall модуля 'es5-ext'
 */
const removePostinstall = () => {
  const virusFilePath = path.normalize(path.join(process.cwd(), 'node_modules/es5-ext/_postinstall.js'));
  if (fs.existsSync(virusFilePath)) {
    console.log(`Удаление файла _postinstall модуля 'es5-ext'`);
    fs.unlinkSync(virusFilePath);
  }
};

removePostinstall();
